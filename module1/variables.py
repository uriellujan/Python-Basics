# This is module1.py where the focus is to demonstrate my ability
# to work with basic/core Python concepts such as variables,
# dynamic typing, and standard operators.


# Variables

# String
first_name = "Uriel"
food = "tacos"
email = "uriel123@fakemail.net"
print(f"Hello {first_name}\nYou like to eat {food}\nYour email is NOT {email}")

# Integer
age = 21
quantity = 3
print(f"You are NOT {age} years old.\nYou have {quantity} bananas.")

# Float
weight = 6.7
miles = .25
print(f"I weigh more than {weight} kilograms.\nA drag strip is usually {miles} miles long.")

# Boolean
is_a_superhero = True
is_a_supervillan = False
print(f"Is Iron Man a superhero? {is_a_superhero}\nIs Thanos a supervillan? {is_a_supervillan}")

# Dynamic Typing
# Dynamic Typing is a core feature of Python. Dynamic Typing is flexible and allows
# variables to change their type which is determined at runtime instead of at declaration.

x = 21
print(f"The value of x is {x} (Integer)")

x = "Twenty One"
print(f"The value of x is {x} (String)")

# Standard Operators in Python

x = 15
y = 4

print(x + y) # Addition
print(x - y) # Subtraction
print(x * y) # Multiplication
print(x / y) # Division
print(x % y) # Modulus (remainder)
print(x ** y) # Exponentiation
print(x // y) # Floor Division

